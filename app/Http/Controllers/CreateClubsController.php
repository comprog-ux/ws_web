     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'name' => 'required',
            'clubs_nr' => 'required',
            'cover_image' => 'image|nullable|max:1999'
        ]);
        
        // Create Post
        $post = new Createclub;
        $post->name = $request->input('name');
        $post->clubs_nr = $request->input('clubs_nr');
        $post->districts_id = $request->input('districts_id');
        
        $post->save();

        return redirect('/skapa')->with('success', 'FÃ¶rening skapad');
    }

